// Helper function to get translation
export function t(key: string, language: Language): string {
  const translation = translations[key];
  if (!translation) {
    console.warn(`Missing translation key: ${key}`);
    return key;
  }
  return translation[language] || translation.en || key;
}

// Helper to get category name in selected language
export function getCategoryName(category: string, language: Language): string {
  const categoryMap: Record<string, string> = {
    burnable: "burnable",
    "non-burnable": "nonBurnable",
    recyclable: "recyclable",
    plastic: "plastic",
    hazardous: "hazardous",
    oversized: "oversized",
  };
  const key = categoryMap[category.toLowerCase()] || category;
  return t(key, language);
}

// Helper to get bag color name in selected language
export function getBagColorName(bagColor: string, language: Language): string {
  const bagMap: Record<string, string> = {
    green: "greenBag",
    transparent: "transparentBag",
    pink: "pinkBag",
    blue: "blueBag",
  };
  const key = bagMap[bagColor.toLowerCase()] || bagColor;
  return t(key, language);
}
